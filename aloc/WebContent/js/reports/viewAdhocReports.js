$(document).ready( function() {
	$('.delete-tr').off('click').on('click', function() {
		var deleteForm = $(this).closest('form.deleteTemplateForm');
		$(deleteForm).submit();
	});
	$('.openTemplate').off('click').on('click', function() {
		var openTemplateForm = $(this).closest('form.openTemplateForm');
		$(openTemplateForm).submit();
	});
	$("table.table tr:odd").addClass("odd");
});